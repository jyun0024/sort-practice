class QuickSort {
    /**
     * ツリーソートを行う関数
     */
    public static void quickSort(int[] numArray) {
        System.out.println("「クイックソート」");

        System.out.print("ソート前：");
        for (int num : numArray) {
            System.out.print(num + ", ");
        }
        System.out.println();
    }

}